    private void btnIzmeniTerminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniTerminActionPerformed

        try {
            if (txtDatumVreme.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Polje za datum i vreme termina mora biti popunjeno!");
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
            Date datumVreme = sdf.parse(txtDatumVreme.getText());

            if (datumVreme.before(new Date())) {
                JOptionPane.showMessageDialog(this, "Datum i vreme mora biti posle sadasnjeg datuma!");
                return;
            }

            double cenaTermina = Double.parseDouble(txtCenaTermina.getText());

            TableModelUsluge tm = (TableModelUsluge) tblUsluge.getModel();

            t.setDatumVreme(datumVreme);
            t.setCenaTermina(cenaTermina);

            for (Usluga usluga : tm.getLista()) {
                usluga.setTermin(t);
            }

            t.setListaUsluga(tm.getLista());

            ClientController.getInstance().updateTermin(t);
            FormPretragaTermina fp = (FormPretragaTermina) getParent();
            fp.refreshTable();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjen termin.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }


    }//GEN-LAST:event_btnIzmeniTerminActionPerformed

    private void btnObrisiTerminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiTerminActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovaj termin?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {

                TableModelUsluge tm = (TableModelUsluge) tblUsluge.getModel();
                t.setListaUsluga(tm.getLista());
                ClientController.getInstance().deleteTermin(t);
                FormPretragaTermina fp = (FormPretragaTermina) getParent();
                fp.refreshTable();
                JOptionPane.showMessageDialog(this, "Uspesno obrisan termin!");
                this.dispose();

            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiTermina.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnObrisiTerminActionPerformed

    private void btnDodajUsluguActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajUsluguActionPerformed

        if (txtCenaUsluge.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Cena usluge mora biti popunjena!");
            return;
        }

        VrstaUsluge vu = (VrstaUsluge) cmbVrstaUsluge.getSelectedItem();
        double cenaUsluge = Double.parseDouble(txtCenaUsluge.getText());

        if (cenaUsluge < 0) {
            JOptionPane.showMessageDialog(this, "Cena usluge ne sme biti negativna!");
            return;
        }

        Usluga u = new Usluga(null, -1, cenaUsluge, vu);

        TableModelUsluge tm = (TableModelUsluge) tblUsluge.getModel();

        if (tm.postojiVrstaUsluge(vu)) {
            JOptionPane.showMessageDialog(this, "Vec je uneta ta vrsta usluge!");
            return;
        }

        tm.dodajUsluguZaIzmenu(u);

        double cenaTermina = Double.parseDouble(txtCenaTermina.getText());

        txtCenaTermina.setText(String.valueOf(cenaTermina + cenaUsluge));
    }//GEN-LAST:event_btnDodajUsluguActionPerformed

    private void btnObrisiUsluguActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiUsluguActionPerformed

        int row = tblUsluge.getSelectedRow();

        if (row >= 0) {

            TableModelUsluge tm = (TableModelUsluge) tblUsluge.getModel();

            double cenaTermina = Double.parseDouble(txtCenaTermina.getText());

            Usluga u = tm.getUsluga(row);

            txtCenaTermina.setText(String.valueOf(cenaTermina - u.getCenaUsluge()));

            tm.obrisiUslugu(row);

        }
    }//GEN-LAST:event_btnObrisiUsluguActionPerformed

    private void btnIzvestajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzvestajActionPerformed

        if (t.getIzvestaj() == null) {
            int result = JOptionPane.showConfirmDialog(this, "Ovaj termin nema izvestaj. Da li "
                    + "zelite da ga unesete?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

            if (result == JOptionPane.NO_OPTION) {
                return;
            }

            if (result == JOptionPane.YES_OPTION) {
                new FormNoviIzvestaj(this, true, t).setVisible(true);
            }

        } else {
            new FormPregledIzvestaja(this, true, t.getIzvestaj()).setVisible(true);
        }

    }//GEN-LAST:event_btnIzvestajActionPerformed

