    private void daLiJeProsaoDatum() {
        if (t.getDatumVreme().before(new Date())) {
            lblObavestenje.setText("Termin je prosao, ne mozete da ga menjate niti brisete.");
            txtDatumVreme.setEnabled(false);
            cmbVrstaUsluge.setEnabled(false);
            txtCenaUsluge.setEnabled(false);
            btnObrisiUslugu.setEnabled(false);
            btnDodajUslugu.setEnabled(false);
            btnObrisiTermin.setEnabled(false);
            btnIzmeniTermin.setEnabled(false);
        }
    }

    private void popuniVrsteUsluga() {
        try {
            ArrayList<VrstaUsluge> vrsteUsluga = ClientController.getInstance().getAllVrstaUsluge();

            cmbVrstaUsluge.removeAllItems();

            for (VrstaUsluge vrstaUsluge : vrsteUsluga) {
                cmbVrstaUsluge.addItem(vrstaUsluge);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

